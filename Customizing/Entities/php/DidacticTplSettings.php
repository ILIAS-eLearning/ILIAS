<?php



/**
 * DidacticTplSettings
 */
class DidacticTplSettings
{
    /**
     * @var int
     */
    private $id = '0';

    /**
     * @var bool
     */
    private $enabled = '0';

    /**
     * @var bool
     */
    private $type = '0';

    /**
     * @var string|null
     */
    private $title;

    /**
     * @var string|null
     */
    private $description;

    /**
     * @var string|null
     */
    private $info;

    /**
     * @var bool
     */
    private $autoGenerated = '0';

    /**
     * @var bool
     */
    private $exclusiveTpl = '0';


    /**
     * Get id.
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set enabled.
     *
     * @param bool $enabled
     *
     * @return DidacticTplSettings
     */
    public function setEnabled($enabled)
    {
        $this->enabled = $enabled;

        return $this;
    }

    /**
     * Get enabled.
     *
     * @return bool
     */
    public function getEnabled()
    {
        return $this->enabled;
    }

    /**
     * Set type.
     *
     * @param bool $type
     *
     * @return DidacticTplSettings
     */
    public function setType($type)
    {
        $this->type = $type;

        return $this;
    }

    /**
     * Get type.
     *
     * @return bool
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * Set title.
     *
     * @param string|null $title
     *
     * @return DidacticTplSettings
     */
    public function setTitle($title = null)
    {
        $this->title = $title;

        return $this;
    }

    /**
     * Get title.
     *
     * @return string|null
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * Set description.
     *
     * @param string|null $description
     *
     * @return DidacticTplSettings
     */
    public function setDescription($description = null)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description.
     *
     * @return string|null
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * Set info.
     *
     * @param string|null $info
     *
     * @return DidacticTplSettings
     */
    public function setInfo($info = null)
    {
        $this->info = $info;

        return $this;
    }

    /**
     * Get info.
     *
     * @return string|null
     */
    public function getInfo()
    {
        return $this->info;
    }

    /**
     * Set autoGenerated.
     *
     * @param bool $autoGenerated
     *
     * @return DidacticTplSettings
     */
    public function setAutoGenerated($autoGenerated)
    {
        $this->autoGenerated = $autoGenerated;

        return $this;
    }

    /**
     * Get autoGenerated.
     *
     * @return bool
     */
    public function getAutoGenerated()
    {
        return $this->autoGenerated;
    }

    /**
     * Set exclusiveTpl.
     *
     * @param bool $exclusiveTpl
     *
     * @return DidacticTplSettings
     */
    public function setExclusiveTpl($exclusiveTpl)
    {
        $this->exclusiveTpl = $exclusiveTpl;

        return $this;
    }

    /**
     * Get exclusiveTpl.
     *
     * @return bool
     */
    public function getExclusiveTpl()
    {
        return $this->exclusiveTpl;
    }
}
