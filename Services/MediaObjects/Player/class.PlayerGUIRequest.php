namespace ILIAS\MediaObjects\Player;

use ILIAS\Repository\BaseGUIRequest;

class PlayerGUIRequest
{
    use BaseGUIRequest;

    public function __construct(
        \ILIAS\HTTP\Services $http,
        \ILIAS\Refinery\Factory $refinery
    ) {
        $this->initRequest(
            $http,
            $refinery
        );
    }

    public function getTrackingRefId(): int
    {
        return $this->int("mob_tracking_ref_id");
    }

    public function getTrackingMobId(): int
    {
        return $this->int("mob_tracking_mob_id");
    }
}
